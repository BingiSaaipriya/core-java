package com.operators;

public class UrinaryIncrandDecroperator {
	public static void main(String[] args) {
		int x=10;
		int y=10;
		System.out.println(x++);
		System.out.println(x+"\n");
		
		System.out.println(++y);
		System.out.println(y);
		
		int c=20;
		int d=20;
		System.out.println(c++);
		System.out.println(c);
		System.out.println(++c);
		System.out.println(c);
		System.out.println(d--);
		System.out.println(d);
		System.out.println(--d);
		System.out.println(d);
		
	}

}
